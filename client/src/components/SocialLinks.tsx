interface SocialLinksProps {
  links: SocialLinksType;
  size?: 'sm' | 'md' | 'lg';
  showLabels?: boolean;
}

export default function SocialLinks({ links, size = 'md', showLabels = false }: SocialLinksProps) {
  const platforms = Object.entries(links).filter(([, url]) => url) as [SocialPlatform, string][];

  if (platforms.length === 0) return null;

  const sizeClasses = {
    sm: 'w-7 h-7 text-xs',
    md: 'w-9 h-9 text-sm',
    lg: 'w-11 h-11 text-base',
  };

  return (
    <div className="flex flex-wrap gap-2">
      {platforms.map(([platform, url]) => {
        const meta = socialMeta[platform];
        const Icon = iconMap[platform];
        if (!meta || !Icon) return null;

        return (
          <a
            key={platform}
            href={url}
            target="_blank"
            rel="noopener noreferrer"
            title={meta.label}
            className={`group flex items-center gap-1.5 rounded-xl border transition-all duration-200 ${meta.bgColor} ${sizeClasses[size]} ${showLabels ? 'px-3' : 'justify-center'}`}
            style={{ textDecoration: 'none' }}
          >
            <span className={`transition-colors duration-200 ${meta.color} ${meta.hoverColor}`}>
              <Icon />
            </span>
            {showLabels && (
              <span className={`text-xs font-semibold transition-colors duration-200 ${meta.color} ${meta.hoverColor}`}>
                {meta.label}
              </span>
            )}
          </a>
        );
      })}
    </div>
  );
}
